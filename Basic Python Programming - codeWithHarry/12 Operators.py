"""
    Operators in python:
        Arithmetic Operators
        Assignment Operators
        Comparison Operators
        Logical Operators
        Membership Operators
        Bitwise Operators
"""
print("5 + 6 = ", 5 + 6)
print("5 - 6 = ", 5 - 6)
print("5 * 6 = ", 5 * 6)
print("5 / 6 = ", 5 / 6)
print("15 // 6 = ", 15 // 6)  # float division operator => it gives round off answer
print("5 ** 6 = ", 5 ** 6)  # exponential operator
print("10 % 2 = ", 10 % 2)

# Assignment Operator:
print("\nAssignment Operators:")
x = 5
print(x)
x += 7
print(x)

# Comparison Operators
#     ==  !=  >  >=  <  <=

# Logical Operators:
#    and, or

# Identity Operators:
#    is => hae, is not => nahi hae

# Membership Operators
print("\nMembership Operators:")
list = [3, 4, 32, 6, 78, 98]
print(32 in list)
print(324 not in list)

# Bitwise Operators
print("\nBitwise Operators:")
print(0 & 2)
print(0 | 3)
