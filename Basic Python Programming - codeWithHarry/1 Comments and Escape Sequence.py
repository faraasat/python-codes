# single line comment
"""Multi
line
comment"""

# if we write only print then it will automatically add a next line
print("Hello ")
print("there!")

# to remove next line:
print("Hello", end=" ")
print("there!")

# to put comma:
print("Hello", end=",")
print("there!")

# another way to write
print("Hello", "there!", end=" ")

# This way only give space
print("Hello", "there!")

"""
    \n => new line
    \t => tab
    \\ => backslash
    \" => Double Quotes
"""